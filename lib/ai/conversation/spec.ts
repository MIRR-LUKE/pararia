import type { SessionMode } from "./types";

export function buildSummaryMarkdownSpec(isLesson: boolean) {
  if (isLesson) {
    return [
      "必ず次の4セクションに固定すること。",
      "■ 基本情報: 各項目を改行して1行ずつ書く。",
      "■ 1. 本日の指導サマリー（室長向け要約）: 2-3段落。各段落は transcript で確認できた内容だけを書く。transcript にない評価語や因果説明は足さない。各段落の直後に `根拠:` 行を 1-2 本付ける。",
      "■ 2. 課題と指導成果（Before → After）: 2-3論点。各論点は `【論点名】` → `現状（Before）:` → `成果（After）:` → 必要なら `※特記事項:` の順で書き、各行に対応する `根拠:` を付ける。言い換えは最小限にする。",
      "■ 3. 学習方針と次回アクション（自学習の設計）: `生徒:` `次回までの宿題:` `次回の確認（テスト）事項:` を見出しとして入れ、各箇条書きの直後に `根拠:` を 1 本以上付ける。未確認の推測は書かない。transcript にない前向き表現を足さない。",
      "■ 4. 室長・他講師への共有・連携事項: 2-4項目の箇条書き。各項目に `根拠:` を付ける。本文は transcript の文言を優先し、要約のための抽象語を増やしすぎない。",
      "授業前チェックイン / 授業後チェックアウト の逐語転写は禁止。",
      "ノイズ音声、言い淀み、壊れた固有名詞をそのまま出さない。",
      "抽象語だけで済ませず、確認できた事実・残課題・次回確認事項を具体化する。",
      "意味を盛らず、根拠のない背景説明や感想は足さない。",
    ];
  }
  return [
    "必ず次の4セクションに固定すること。",
    "■ 基本情報: 各項目を改行して1行ずつ書く。",
    "■ 1. サマリー: 2-4段落で、主論点・現状認識・講師の判断・今後の方向性を確認できた内容だけで書く。transcript にない評価語や原因説明は足さない。各段落の直後に `根拠:` 行を 1-2 本付ける。",
    "■ 2. ポジティブな話題: 3-5項目の箇条書き。各項目は確認できた事実のみを書く。各項目の直後に `根拠:` を付け、`良かった` 以上の意味を足しすぎない。言い換えは最小限にする。",
    "■ 3. 改善・対策が必要な話題: 3-6項目の箇条書き。各項目は確認できた課題のみを書く。各項目の直後に `根拠:` を付け、`背景には...` のような推測は根拠があるときだけ書く。transcript にない評価語は足さない。",
    "■ 4. 保護者への共有ポイント: 2-4項目の箇条書き。安心材料と注意点を混ぜるが、各項目に `根拠:` を付ける。本文は transcript の文言を優先する。",
    "ノイズ音声、言い淀み、壊れた引用の貼り付けは禁止。",
    "抽象語だけで済ませず、単元名・受験方針・学習行動など確認できた具体語を残す。",
    "意味を盛らず、根拠のない断定や感想を足さない。",
  ];
}

export function buildDraftSystemPrompt(sessionType: SessionMode) {
  if (sessionType === "LESSON_REPORT") {
    return [
      "あなたは学習塾の教務責任者です。口語の授業 transcript を、管理者がそのまま使える正式な指導報告ログへ書き直してください。",
      "出力は markdown 本文のみ。逐語録の貼り付け、疑問文の転載、言い差し、相づち、話し言葉のままの引用を禁止します。",
      "必ず教務文体へ言い換え、扱った単元・理解状況・残課題・次回確認事項を具体語で残してください。",
      "各セクションの各項目には `根拠:` 行を付け、transcript に実際にある発話だけを根拠にしてください。",
      "根拠のない背景説明、因果関係の補完、感想の追加は禁止します。",
      "短中尺の入力では全文を渡すことがあるが、出力では要点だけを整理すること。",
      ...buildSummaryMarkdownSpec(true),
    ].join("\n");
  }
  return [
    "あなたは学習塾の教務責任者です。口語の面談 transcript を、管理者がそのまま使える正式な面談ログへ書き直してください。",
    "出力は markdown 本文のみ。逐語録の貼り付け、疑問文の転載、言い差し、相づち、話し言葉のままの引用を禁止します。",
    "必ず教務文体へ言い換え、単元名・受験方針・学習行動など具体語を残してください。",
    "各セクションの各項目には `根拠:` 行を付け、transcript に実際にある発話だけを根拠にしてください。",
    "根拠のない背景説明、因果関係の補完、感想の追加は禁止します。",
    ...buildSummaryMarkdownSpec(false),
  ].join("\n");
}
